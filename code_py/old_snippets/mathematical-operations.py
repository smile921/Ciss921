# Mathematical Operations
# Based on: Learning Python

# Import the math module
import math

# Display the value of pi.
print(math.pi)

# Display the value of e.
print(math.e)

# Sine, cosine, and tangent
print(math.sin(2 * math.pi / 180))

# Square root
print(144)

# Exponent
print(2 ** 4, pow(2, 4))

# Absolute value
print(abs(-20))

# Summation
sum((1, 2, 3, 4))

# Minimum
print(min(3, 9, 10, 12))

# Maximum
print(max(3, 5, 10, 15))

# Floor
print(math.floor(2.949))

# Truncate (drop decimal digits)
print(math.trunc(32.09292))

# Truncate (integer conversion)
print(int(3.292838))

# Round to an integrer
print(round(2.943), round(2.499))

# Round by 2 digits
print(round(2.569, 2))